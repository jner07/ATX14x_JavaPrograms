public class Lab004_For_If_else {
    public static void main(String[] args) {
        for (int Jayesh = 0; Jayesh < 18; Jayesh++) {
            if (Jayesh >= 16){
                System.out.println("You can get a phone");
            } else {
                System.out.println("you have to wait till 18");
            }
            
        }
    }
}
