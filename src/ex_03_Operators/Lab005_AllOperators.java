package ex_03_Operators;

public class Lab005_AllOperators {
    public static void main(String[] args) {
        int age =10;
        age +=10;
        System.out.println(age); // += acts as adding the same value as literal (10+10)

        int age1=20;
        age1 -=10;
        System.out.println(age1); //-= acts as adding the same value as literal (20+10)

        int age2 = 40;
        age2 /= 10;
        System.out.println(age2); // divide

    }
}
