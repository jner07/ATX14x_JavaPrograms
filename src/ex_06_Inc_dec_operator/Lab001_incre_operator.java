package ex_06_Inc_dec_operator;

public class Lab001_incre_operator {
    public static void main(String[] args) {
        int a = 10;
        int b = ++a;
        System.out.println(a);
        System.out.println(b);
        //++a will increment the value of a and then print it for b hence both will have 11 11
    }
}
